def _percentile(values: list[int], q: float) -> int:
    if not values:
        return 0
    ordered = sorted(values)
    if len(ordered) == 1:
        return ordered[0]
    rank = int(math.ceil(q / 100.0 * len(ordered))) - 1
    rank = max(0, min(rank, len(ordered) - 1))
    return ordered[rank]


def summarize(outcomes: list[CaseArmOutcome], arms: list[str]) -> dict:
    per_arm: dict[str, dict] = {}
    for arm in arms:
        arm_outcomes = [o for o in outcomes if o.arm == arm]
        graded = [o for o in arm_outcomes if o.graded]
        passed = [o for o in graded if o.passed]
        skipped = [o for o in arm_outcomes if o.status == STATUS_SKIPPED]
        latencies = [o.latency_ms for o in graded]
        pass_rate = (len(passed) / len(graded)) if graded else 0.0
        per_arm[arm] = {
            "cases": len(arm_outcomes),
            "graded": len(graded),
            "passed": len(passed),
            "failed": len(graded) - len(passed),
            "skipped": len(skipped),
            "safetyFailures": sum(1 for o in graded if o.safety_failed),
            "expectedFailures": sum(1 for o in graded if o.expected_failed),
            "passRate": round(pass_rate, 4),
            "p50LatencyMs": _percentile(latencies, 50),
            "p95LatencyMs": _percentile(latencies, 95),
        }
    graded_all = [o for o in outcomes if o.graded]
    passed_all = [o for o in graded_all if o.passed]
    overall = {
        "cases": len(outcomes),
        "graded": len(graded_all),
        "passed": len(passed_all),
        "failed": len(graded_all) - len(passed_all),
        "skipped": sum(1 for o in outcomes if o.status == STATUS_SKIPPED),
        "safetyFailures": sum(1 for o in graded_all if o.safety_failed),
        "expectedFailures": sum(1 for o in graded_all if o.expected_failed),
        "passRate": round((len(passed_all) / len(graded_all)) if graded_all else 0.0, 4),
    }
    return {"perArm": per_arm, "overall": overall}


def _validation_payload(report: Optional[ValidationReport]) -> Optional[dict]:
    if report is None:
        return None
    return {
        "passed": report.passed,
        "violations": [
            {"category": v.category, "code": v.code, "detail": v.detail} for v in report.violations
        ],
    }


def _result_payload(outcome: CaseArmOutcome) -> Optional[dict]:
    if outcome.result is None:
        return None
    return outcome.result.model_dump(exclude_none=True)


def build_records(outcomes: list[CaseArmOutcome], cfg: RunConfig) -> list[dict]:
    records: list[dict] = []
    records.append(
        {
            "schemaVersion": RUN_SCHEMA_VERSION,
            "recordType": "run",
            "run": {
                "runId": cfg.run_id,
                "mode": cfg.mode,
                "arms": cfg.arms,
                "model": cfg.model,
                "promptRevision": cfg.prompt_revision,
                "toolTransport": cfg.tool_transport,
                "probeEvidence": cfg.probe_evidence,
            },
        }
    )
    for outcome in outcomes:
        records.append(
            {
                "schemaVersion": RUN_SCHEMA_VERSION,
                "recordType": "case",
                "caseId": outcome.case_id,
                "arm": outcome.arm,
                "status": outcome.status,
                "skipReason": outcome.skip_reason or None,
                "passed": outcome.passed,
                "latencyMs": outcome.latency_ms,
                "result": _result_payload(outcome),
                "validation": _validation_payload(outcome.validation),
                "graders": [
                    {"name": g.name, "passed": g.passed, "detail": g.detail} for g in outcome.grades
                ],
            }
        )
    records.append(
        {
            "schemaVersion": RUN_SCHEMA_VERSION,
            "recordType": "summary",
            "run": {"runId": cfg.run_id, "mode": cfg.mode},
            "totals": summarize(outcomes, cfg.arms),
        }
    )
    return records


def write_artifact(path: str, records: list[dict], api_key: str = "") -> None:
    def encode(record: dict) -> str:
        raw = json.dumps(record, ensure_ascii=False)
        # Only literal-replace a key long enough to be a real secret. A short
        # value (e.g. an empty or truncated key) would blanket-redact ordinary
        # substrings across every artifact line; the regex pattern below still
        # catches genuinely secret-shaped tokens regardless. Mirrors the Go
        # harness redaction guard.
        if api_key and len(api_key) >= 8:
            raw = raw.replace(api_key, "[REDACTED]")
        raw = _ARTIFACT_SECRET_PATTERN.sub("[REDACTED]", raw)
        return raw

    lines = [encode(record) for record in records]
    if path == "-":
        import sys

        sys.stdout.write("\n".join(lines) + "\n")
        return
    out = Path(path)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text("\n".join(lines) + "\n")
